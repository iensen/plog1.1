//Read the input file and add '$' to the end
bool CParse::ReadFile(string fileName)
{
    ifstream infile;
	infile.open(fileName.c_str(), std::ios::in|std::ios::binary|std::ios::ate);
	//readPos=0;
	if (infile.is_open())
	{
		memSize = infile.tellg();
		memblock = new char [memSize+1];
		infile.seekg (0, std::ios::beg);  //Move read point to the head of the file
		infile.read (memblock, memSize);  //Read the whole program into the memory block
		infile.close();					  //Close file
		memblock[memSize]='$';			  //Add the ending symbol 
		return true;
	}
	else return false;
	
}


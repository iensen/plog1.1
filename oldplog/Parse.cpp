//Read Files and Get the next token
//Return the token and set current token type
string CParse::GetNextToken()
{
	string sname="";
	//Ignoring comments and spaces and other unuseful characters
	while(readPos<=memSize){
		if(memblock[readPos]=='%'){
			readPos++;
			while(memblock[readPos]!='\n' && readPos<memSize){
				readPos++;
			}
		}
		if(!isprint(memblock[readPos]) || memblock[readPos]==' ') readPos++;
		else break;
	}
	//Indentifier and variable
	if(isalpha(memblock[readPos])){
		do{
			sname.insert(sname.size(),1,memblock[readPos]);
			readPos++;
		}while(isdigit(memblock[readPos]) || isalpha(memblock[readPos]) || memblock[readPos]=='_');
	}
	else if(isdigit(memblock[readPos])){//constant
		do{
			sname.insert(sname.size(),1,memblock[readPos]);
			readPos++;
		}while(isdigit(memblock[readPos]));
	}
	else{
		if(memblock[readPos]=='>' && memblock[readPos+1]=='='){ sname=">="; readPos++;}
		else if(memblock[readPos]==':' && memblock[readPos+1]=='-') { sname=":-"; readPos++;}
		else if(memblock[readPos]=='<' && memblock[readPos+1]=='>') { sname="<>"; readPos++;}
		else if(memblock[readPos]=='<' && memblock[readPos+1]=='=') { sname="<="; readPos++;}
		else if(memblock[readPos]=='-' && memblock[readPos+1]=='>') { sname="->"; readPos++;}
		else if(memblock[readPos]=='.' && memblock[readPos+1]=='.') { sname=".."; readPos++;}
		else if(memblock[readPos]=='=' && memblock[readPos+1]=='=') { sname="=="; readPos++;}
		else if(memblock[readPos]=='+' && memblock[readPos+1]=='-') { sname="+-"; readPos++;}
		else	sname.insert(sname.size(),1,memblock[readPos]);
		readPos++;
	}
	//Set current symbol id
	if(symbolTable.find(sname)!=symbolTable.end()) symbolId=symbolTable[sname];
	else{
		if(isupper(sname[0])) symbolId=26; //Variable , terminal
		else if(islower(sname[0])) symbolId=19;  //indentifer,terminal
		else if(isdigit(sname[0])) symbolId=14; //Constant
		else symbolId=0; //error happened, due to unknown symbol in the program or query
	}
	return sname;
}

int CParse::SyntaxAnalysis(string fileName)
{
	//std::vector<Symbols*>::iterator itoken;
	string tokenName;
	std::stack<int> stk;
	int j,k;
	int istate=0;
	bool iresult=false;
	if(!ReadFile(fileName)){
		cout<<"Unable to open the input file"<<endl;
		cout<<"Program termintated"<<endl;
		return -1; //File do not found
	}
	stk.push(0);
	tokenName=GetNextToken();
	do{
		if(shift[istate][symbolId]!=-1){
			//std::cout<<"shift "<<istate<<" "<<(*itoken)->name<<" "<<shift[istate][(*itoken)->type]<<std::endl;
			ShiftAction(istate,tokenName);
			stk.push(symbolId);
			istate=shift[istate][symbolId];
			stk.push(istate);
			tokenName=GetNextToken();
		}
		else if(reduce[istate][symbolId]!=-1)
		{
			//std::cout<<"reduce "<<istate<<" "<<(*itoken)->name<<" "<<reduce[istate][(*itoken)->type]<<std::endl;
			if(reduce[istate][symbolId]==0){
				iresult=true;
				break;
			}
			if(!ReduceAction(reduce[istate][symbolId])) return -1;
			for(j=0;j<2*igr[reduce[istate][symbolId]][1];j++) stk.pop();
			j=stk.top();
			k=igr[reduce[istate][symbolId]][0];
			istate=shift[j][k];
			stk.push(k);
			stk.push(istate);
		}
		else {
			break;
		}
	}while(iresult==false);
	//------------------------------------------------------------
	if(iresult==true){
		std::cout<<"Syntax Parse Succeed"<<std::endl;
		//prg->sym=new std::vector<Symbols>(symbol.size());
		//std::copy(symbol.begin(),symbol.end(),prg->sym->begin());
		//ShowSymbolTable();
		//prg->ShowProgram();
	}
	else{
		//Contains some parse error
		ErrorReport();
		std::cout<<"Parsing error" ;
		std::cout<<std::endl;
	}
	//delete [] memblock;
	return iresult;
}


